  constructor() {
    effect(() => {
      const lang = this.currentLang();
      const dir = lang === 'ar' ? 'rtl' : 'ltr';
      document.documentElement.setAttribute('dir', dir);
      document.documentElement.setAttribute('lang', lang);
      document.body.setAttribute('dir', dir);
      document.body.classList.toggle('dir-rtl', dir === 'rtl');
      document.body.classList.toggle('dir-ltr', dir === 'ltr');
      localStorage.setItem(I18nService.STORAGE_KEY, lang);
    });
  }

  getRoleLabel(role?: string): string {
    if (!role) return this.t('role.unknown');
    const roleKeyMap: Record<string, string> = {
      'system-admin': 'role.systemadmin',
      'owner-admin': 'role.owner',
      'accountant': 'role.accountant',
      'sales-manager': 'role.salesmanager',
      'employee-manager': 'role.employeemanager',
      'employee': 'role.employee',
      'sales-rep': 'role.sales_rep'
    };
    const key = roleKeyMap[role] || 'role.unknown';
    return this.t(key);
  }

  getWorkspaceName(name?: string): string {
    if (!name) return '';
    const nameClean = name.trim();
    if (nameClean === 'لوحة التحكم الفنية' || nameClean === 'System Administration Panel' || nameClean === 'نظام رصد الذكي' || nameClean === 'System Admin Panel') {
      return this.t('workspace.sysadmin');
    }
    if (nameClean === 'مؤسسة رصد لتقنية المعلومات' || nameClean === 'Rasd Tech Solutions' || nameClean === 'مؤسسة رصد لتقنية المعلومات (تجريبي)') {
      return this.t('workspace.default');
    }
    return name;
  }

  t(key: string): string {
    return this.translations[this.currentLang()][key] || key;
  }

  tFormat(key: string, params: Record<string, string>): string {
    let str = this.t(key);
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{${k}}`, v);
    }
    return str;
  }

  toggleLang() {
    this.currentLang.update(lang => lang === 'ar' ? 'en' : 'ar');
  }

  setLang(lang: Lang) {
    this.currentLang.set(lang);
  }

  private loadLang(): Lang {
    const saved = localStorage.getItem(I18nService.STORAGE_KEY);
    if (saved === 'en' || saved === 'ar') return saved;
    return 'ar'; // Default Arabic
  }
